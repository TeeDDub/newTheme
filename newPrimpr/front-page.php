  for (var i = 0; i < images.length; i++) {
    preimage[i] = new Image();
    preimage[i].src = images[i];
  }
}


function doSlideshow(){
  var img=new Image();
  img.src='https://i2.wp.com/pressimpress.kr/wp-content/uploads/2017/08/KakaoTalk_2017-08-01-17-33-21_Photo_86.jpeg';
